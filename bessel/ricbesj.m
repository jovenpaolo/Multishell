function S = ricbesj( nu, z )
%RICBESJ calculates the Riccati-Bessel function of the first kind
% -------------------------------------------------------------------------
%% INPUT:
% -------------------------------------------------------------------------
% nu - the order of Ricatti-Bessel function
% z  - argument
% -------------------------------------------------------------------------
%% OUTPUT
% -------------------------------------------------------------------------
% S - Ricatti-Bessel function of the first kind
% -------------------------------------------------------------------------
%% CALCULATING S
% -------------------------------------------------------------------------
S = sqrt( pi*z/2 ).*besselj( nu+0.5, z );
% -------------------------------------------------------------------------
end