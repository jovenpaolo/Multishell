function h = sbesselh( nu, k ,z )
%SBESSELH calculates spherical Bessel function of the third kind
% -------------------------------------------------------------------------
%% INPUT:
% -------------------------------------------------------------------------
% nu - the order of spherical Bessel function
% k  - 1 or 2, default = 1
% z  - argument
% -------------------------------------------------------------------------
%% OUTPUT
% -------------------------------------------------------------------------
% h - spherical Bessel function of the first kind
% -------------------------------------------------------------------------
%% CHECKING INPUT
% -------------------------------------------------------------------------
if nargin == 2
    z = k;
    k = 1;
end
% -------------------------------------------------------------------------
%% CALCULATING h
% -------------------------------------------------------------------------    
h = ricbesh( nu, k, z )./z;
% -------------------------------------------------------------------------    
end